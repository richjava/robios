    function checkLogin() {
        var isLoggedIn = false;
        $.post('auth/is_logged_in')
                .done(function (data) {
                    isLoggedIn = data;
                });
        isLoggedIn = isLoggedIn === 'true';
        if (!isLoggedIn) {
            $("#loginModal").modal("show");
        }
        return isLoggedIn;
